public class Figures {
    protected String name;
    protected String sign;

    public Figures(String name, String sign) {
        this.name = name;
        this.sign = sign;
    }

    public String getSign() {
        return this.sign;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }
}
